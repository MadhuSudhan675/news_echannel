// Function to create a card element
function createCard(heading, image) {
  var cardDiv = document.createElement("div");
  cardDiv.classList.add("card");

  var headingElem = document.createElement("h1");
  headingElem.classList.add("card-heading");
  headingElem.textContent = heading;

  var imgElem = document.createElement("img");
  imgElem.classList.add("card-img");
  imgElem.src = image;
  imgElem.alt = "";

  cardDiv.appendChild(headingElem);
  cardDiv.appendChild(imgElem);

  return cardDiv;
}

// Function to add cards to the container
function addCardsToContainer(container, data) {
  data.forEach(function (item) {
    console.log(item);
    var card = createCard(item.heading, item.image);
    container.appendChild(card);
  });
}

